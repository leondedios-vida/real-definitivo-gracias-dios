export const getStaticRevelation = (sealIndex: number, toneIndex: number): { identityPhrase: string, mysteryHook: string } => {
  const seal = SEAL_ARCHETYPES[sealIndex];
  const tone = TONE_ARCHETYPES[toneIndex];

  // Construcción determinista pero natural
  // Identity: [Seal Identity] + [Tone Nuance]
  const baseIdentity = seal.identity.endsWith('.') ? seal.identity.slice(0, -1) : seal.identity;
  const identityPhrase = `${baseIdentity}, ${tone.nuance}`;

  // Hook: "En tu aspecto sombra, [Seal Shadow lowercase] + [Tone Friction]"
  // Esto convierte la declaración absoluta en un estado condicional.
  
  let baseShadow = seal.shadow.endsWith('.') ? seal.shadow.slice(0, -1) : seal.shadow;
  // Descapitalizar la primera letra para que fluya en la frase, a menos que sea un nombre propio (raro en estos textos).
  baseShadow = baseShadow.charAt(0).toLowerCase() + baseShadow.slice(1);

  const mysteryHook = `En tu aspecto sombra, ${baseShadow}, ${tone.friction}`;

  return {
    identityPhrase,
    mysteryHook
  };
};
